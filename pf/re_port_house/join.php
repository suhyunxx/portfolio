                                제9조(통관) <br/>

                                ① 회사는 이용자를 위하여 회사가 정한 운송 및 통관 업무 위탁사를 통해 통관절차를 수행합니다. 
                                이 때 발생하는 관·부가세 등은 이용자 또는 수하인이 부담합니다.<br/>



                                ② 제1항의 통관 업무를 수행할 때에, 회사가 정한 운송 및 통관 업무 위탁사가 
